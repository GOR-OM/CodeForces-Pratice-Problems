void solve(int x ,int y, int n ,vector<int>&ans){
    for(int d=1; d<=y-x;d++){
        vector<int>temp;
        int val = y ;
        while(temp.size()<n && val>0){
            temp.pb(val);
            val-=d;
        }
        if(temp.size()!=n){
            val = y+d;
            while(temp.size()<n){
                temp.pb(val);
                val+=d;
            }
        }
        if(temp.size()==n && find(temp.begin(),temp.end(),x)!=temp.end() ){
            ans = temp;
            return ;
        } ; 
    }
}








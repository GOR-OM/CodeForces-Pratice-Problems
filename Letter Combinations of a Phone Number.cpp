void solve(int i, int n ,string digits , string s, vector<string> &ans, vector<string> mp)
{
    if(i == n)
    {
        ans.push_back(s);
        return;
    }
    for(auto c : mp[digits[i] - '0'])
    {
        s.push_back(c);
        solve(i + 1, n, digits, s, ans, mp);
        s.pop_back();
    }
}



long long solve(vector<long long> &arr)
{
    int n = arr.size();
    long long mul = (long long)arr[0] * arr[n - 1];

    for (int i = 0; i < n; i++)
    {
        if ((long long)arr[i] * arr[n - i - 1] != mul)
        {
            return -1;
        }
        if (i == n / 2)
        {
            break;
        }
    }
    for(long long i=2 ; i*i<=mul ; i++){
        if(mul%i==0){
            if(find(arr.begin(),arr.end(),i)==arr.end()){
                return -1;
            }
        }
    }
    return mul;
}


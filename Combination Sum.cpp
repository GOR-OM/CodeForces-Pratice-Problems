void solve(int index,int target,  int n, vector<int>& temp , vector<vector<int>> &ans,vector<int>&arr){
    if(index == arr.size()){
        if(target==0){
            ans.push_back(temp);
        }
    }

    if(arr[index]<=target){
        temp.push_back(arr[index]);
        solve(index,target-arr[index],n,temp,ans,arr);
        temp.pop_back();
    }
    solve(index+1,target,n,temp,ans,arr);
}


